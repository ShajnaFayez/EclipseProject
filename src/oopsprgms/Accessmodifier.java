package oopsprgms;

public class Accessmodifier {
	
	int a;
	private int b;
	protected int c;
	public int d;

	public static void main(String[] args) {
	    
		Accessmodifier ob=new Accessmodifier();
		System.out.println(ob.a);
		System.out.println(ob.b);
		System.out.println(ob.c);
		System.out.println(ob.d);

	}
	 
	

}
